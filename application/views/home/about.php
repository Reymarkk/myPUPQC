                          <p>
                            The Polytechnic University of the Philippines Quezon City (abbreviated as PUPQC; also known as PUP Commonwealth Campus) is one of the satellite campuses of the Polytechnic University of the Philippines located in Don Fabian Street., Barangay Commonwealth, Quezon City, Philippines. It was established in 1997. It confers undergraduate and diploma degrees.
                          </p>
                          <p>
                            The vision of PUP QC is to become a committed and comprehensive institution of learning that provides a culture of excellence. It is to become the pioneering and leading National Polytechnic University in the 21st century. With a National Polytechnic University status, it will bring more opportunities for the university to further strengthen its academic programs and improve organizational performance as an institution for a higher education.
                          </p>
                          <p>
                            In pursuit of this vision, the mission of PUP QC is to become responsive to the needs of its stakeholders by being committed in promoting the cultural and academic excellence guided with ethical and moral leadership in pursuit of quality education. It should recognize its catalytic role for national development. PUP QC will ensure inclusive and equitable quality education and promote life-long learning opportunities through re-engineered polytechnic education. By re-engineering, existing processes and programs are looked at in order to achieve higher quality of academic programs and services, following the principles of effectiveness and efficiency.
                          </p>
                          <p>
                            With this mission in mind, PUP QC follows the 10 pillars as a reform agenda for the university.
                          <ul>
                            <li>Pillar 1: Dynamic, Transformational and Responsible Leadership.</li>
                            <li>Pillar 2: Responsive and Innovative Curricula and Instruction.</li>
                            <li>Pillar 3: Enabling and Productive Learning Environment.</li>
                            <li>Pillar 4: Holistic Student Development and Engagement.</li>
                            <li>Pillar 5: Empowered Faculty Members and Employees.</li>
                            <li>Pillar 6: Vigorous Research Production and Utilization.</li>
                            <li>Pillar 7: Global Academic Standards and Excellence.</li>
                            <li>Pillar 8: Synergistic, Productive, Strategic Networks and Partnerships.</li>
                            <li>Pillar 9: Active and Sustained Stakeholders' Engagement</li>
                            <li>Pillar 10: Sustainable Social Development Programs and Projects.</li>
                          </ul>
                          </p>
                          <div class="mb-3 d-flex justify-content-center">
                            <img src="<?= base_url() ?>public/images/about/img-2.webp" alt="PUPQC" class="img-fluid">
