                          <p>
                            PUPQC was established through the generosity and benevolence of Walter Rothlehner, a German church leader who donated his property in Barangay Commonwealth, Quezon City to PUP. The campus lot with an area of 1.9 hectares is donated by SIKHAY, an association led by Rev. Fr. Joel Tabora. The PUP Open University was tasked to administer and maintain the campus and its facilities. PUPQC was formally inaugurated on July 29, 1997. Although it was in existing prior to 1997, it only started its operations and formal classes after its inauguration.
                          </p>
                          <p>
                            The goals and objectives of PUP Quezon City are as follows:
                          <ul>
                            <li>Transparency and Efficiency<br>
                              Responsibly manage school-related activities that may contribute to the holistic development of all stakeholders in instruction, research, and extension.
                            </li>
                            <li>System Performance<br>
                              Utilize leadership, collaboration, and strategic partnership in institutional, national, and international activities.
                            </li>
                            <li>Organizational Excellence<br>
                              Taking accountability in delivering quality service through excellent performance of faculty members and administrative staff.
                            </li>
                            <li>Safety and Health<br>
                              Promote health through worthwhile school activities involving faculty members, staffs, and students.
                            </li>
                          </ul>
                          </p>
                          <p>
                            PUPQC Branch main objective is to extend quality education to the youth of the marginalized sector of our society. Through the years, it has already produced several graduates who are successful in their chosen field. Likewise, these alumni continue to practice what they have learned in our Sintang Paaralan as indicated in the lyrics of PUP Hymn "Gagamitin ang karunungan mula sa iyo para sa bayan" for the betterment of our country.
                          </p>
                          <p>
                            Its Faculty Members are encouraged to have further educations (such as Master's Degree and Doctorate Degree), conduct research, and attend seminars and training to update with the needs of the industry in the preparation of their students for their chosen career.
                            Its Administrative Staff, likewise, are sent to seminars and training related to their duties and responsibilities to ensure the delivery of quality service to stakeholders.
                            PUPQC Branch commits through its qualified, competent, and dedicated Faculty Members and Administrative Staff to continue to be the way for all less privileged but deserving students in grasping their dreams.
                          </p>
