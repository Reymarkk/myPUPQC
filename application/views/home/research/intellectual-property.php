                        <h3 class="mb-3 text-primary">Intellectual Property Management Office (IPMO)</h3>
                        <div class="mx-3">
                          <p>
                            The Intellectual Property Management Office (IPMO) provides assistance to researchers to secure attractive returns in the long term from the bulk of valuable research outcomes and intellectual property. It also assists researchers in identifying partners within the academe, other businesses and investors to promote translation of research innovations into new products through advice and support for early-stage research and development. The IPMO provides a portfolio of new technologies created through University research for licensing or collaborative development.
                          </p>
