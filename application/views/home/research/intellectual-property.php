                            <h5 class="text-primary fw-bold">IPMO Office and Center (Only in PUP Sta. Mesa)</h5>
                            <ul>
                              <li><span class="fw-medium text-primary">Innovations and Technology Support Office</span><br>
                                The Innovations Technological and Support Office (ITSO) was created to reaffirm PUP's commitment of responding to the needs of time by propagating and protecting intellectual property rights. Intellectual properties are creations of human minds and just like any other form of property, intellectual property rights are duly recognized</li>
                              <li><span class="fw-medium text-primary">Center for Technology Transfer and Enterprise Development</span><br>
                                The Center for Technology Transfer and Enterprise Development (CTTED), is a dynamic center that facilitates and supports the development of start-up technologies based on University research. The Center also addresses the potential roles, considerations, and opportunities for university-affiliated inventors such as students, faculty members and administrative staff.</li>
                            </ul><hr>
                            <h5 class="text-primary fw-bold">Copyright Registration - Application Procedure & Forms</h5>
                                                        <p>As approved in the 141st Regular Meeting of the University Board of Regents and pursuant to the PUP Memorandum Order No. 031, series of 2013, all academic works (theses, dissertations, feasibility studies, design prototypes, computer programs and software, audiovisual and cinematographic works, literary and creative works, etc.) submitted in partial fulfillment of the requirements of undergraduate and graduate courses in the University shall be applied for Copyright Registration with the Polytechnic University of the Philippines as copyright co-owner. The following is the procedure for copyright registration application:</p>
                                                        <p><span class="fw-medium">Step 1:</span>
                                                            Secure the Application Form and Affidavit of Copyright Co-ownership from the Intellectual Property Management Office (IPMO) , or the Innovation and Technology Commercialization Office (ITSO) and Center for Technology Transfer and Enterprise Development for Branches and Satellite Campuses at S-407, or download it from the PUP website at <a href="http://www.pup.edu.ph/research/ipmo" class="text-primary">http://www.pup.edu.ph/research/ipmo.</a> Note that the application for copyright registration of works in science, engineering and information technology shall be handled by ITSO. All the rest shall be coursed through the IPMO.</p>
                                                        <p><span class="fw-medium">Step 2:</span>
                                                            Fill out the forms. For theses/dissertations and other works authored by student/s and submitted to the Polytechnic University of the Philippines in partial fulfillment of the requirements of a course/program, the University must be named as copyright co-owner. The author/s must accomplish/sign the Affidavit on Copyright Co-ownership with the Polytechnic University of the Philippines.</p>
                                                        <p><span class="fw-medium">Step 3:</span>
                                                            Have the accomplished Application Form and Affidavit on Copyright Co-ownership duly notarized.</p>
                                                        <p><span class="fw-medium">Step 4:</span>
                                                            Proceed to the Accounting Office for the Order of Payment.</p>
                                                        <p><span class="fw-medium">Step 5:</span>
                                                            Pay the copyright application fee at the Cashier's Office.</p>
                                                        <p><span class="fw-medium">Step 6:</span>
                                                            Submit the following to IPMO or ITSO: an CTTED.</p>
                                                        <ul>
                                                            <li>Triplicate copies of notarized Co-Authorship Agreement;</li>
                                                            <li>Triplicate copies of the notarized Application Form;</li>
                                                            <li>Triplicate copies of the Affidavit of Copyright Co-ownership;</li>
                                                            <li>Triplicate copies of the document/s (softcopy in CDs) as deposit to the National Library of the Philippines;</li>
                                                            <li>A copy of Approval sheet with signatures.</li>
                                                            <li>Technical description of the design, if the work applied for registration is an original ornamental design (classification H); and</li>
                                                            <li>Official receipt of filing fee from PUP.</li>
                                                        </ul>
                                                        <p><span class="fw-medium">Step 7:</span>
                                                            IPMO / ITSO /CTTED shall acknowledge the submission of the application, and advise the author/s to return on a specified date to claim the Certificate of Copyright Registration issued by the National Library of the Philippines.</p>
