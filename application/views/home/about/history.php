                              <li> Bachelor of Science in Business Administration major in Marketing Management</li>
                              <li> Bachelor of Science in Business Administration major in Human Resource Management</li>
                              <li> Bachelor of Science in Entrepreneurship</li>
                              <li> Diploma in Office Management Technology with specialization in Medical Office Management</li>
                            </ul>
                            <h5 class="text-primary mt-1">Graduate Studies Programs under the PUP Open University System</h5>
                            <ul class="mx-4">
                              <li> Master in Information Technology</li>
                              <li> Master in Educational Management</li>
                              <li> Master in Public Administration</li>
