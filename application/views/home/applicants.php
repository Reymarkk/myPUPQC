                          <h4 class="text-primary">Who are qualified to take the CAEPUP?</h4>
                          <p>Those qualified to apply as first years are:</p>
                          <ol>
                            <li>A Grade 12 student expected to graduate at the end of AY 2021-2022; and those who graduated from K-12 pilot schools and have not enrolled in any technical/diploma/degree programs after graduation.</li>
                            <li>
                              Must have final grades for two (2) high school years (Grade 10 and 11) preceding the year of graduation as follows:
